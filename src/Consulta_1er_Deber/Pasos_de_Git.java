package Consulta_1er_Deber;

public class Pasos_de_Git {
    public class Pasos_Git {
        public static void main(String[] args) {
            System.out.println("Lista de Comandos de Git\n");
            System.out.println("git init");
            System.out.println("Creará un nuevo repositorio local GIT. El siguiente comando de Git creará un repositorio en el directorio actual");
            System.out.println("git add");
            System.out.println("Se usa para agregar archivos al área de preparación. Por ejemplo, el siguiente comando de Git básico indexará el archivo temp.txt:");
            System.out.println("git commit");
            System.out.println("Creará una instantánea de los cambios y la guardará en el directorio git.");
            System.out.println("git branch");
            System.out.println("Se usa para listar, crear o borrar ramas. Por ejemplo, si quieres listar todas las ramas presentes en el repositorio");
            System.out.println("git push -u origin main");
            System.out.println("Se usa para enviar confirmaciones locales a la rama maestra del repositorio remoto");
        }
    }
}
